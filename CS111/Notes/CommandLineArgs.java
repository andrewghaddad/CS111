// will explain how to input data into programs using command line

public class CommandLineArgs {
	
	public static void main(String[] args) { // args= argument (this is the command line)

		System.out.println(args[0]); // this is first argument
		System.out.println(args[1]); // this is second argument
		System.out.println(args[2]); // this is third argument

	 	/*
	 	this is a way to input a value into a code through command line
	 	*/
	}
}